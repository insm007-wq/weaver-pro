/** ÏóÖÎ°úÎìú Ï†ïÏ±Ö */
const MAX_UPLOAD_MB = 10; // 10MBÎ°ú Ï†úÌïú

/** ÌîÑÎ°¨ÌîÑÌä∏ ÌÖúÌîåÎ¶ø Í∏∞Î≥∏Í∞í */
